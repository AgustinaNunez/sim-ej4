    private void chb_distribucionPorDefectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chb_distribucionPorDefectoActionPerformed
        lbl_error.setVisible(false);
        DefaultTableModel modelo;
        if (chb_distribucionPorDefecto.isSelected()) {
            habilitarCampos(false);
            modelo = Distribucion.getDistribucionPredeterminada();
            modelo.setColumnCount(2);
            tbl_distribuciones.setModel(modelo);
            return;
        }
        habilitarCampos(true);
        String[] cabecera = {"Pasajeros", "Prob."};
        modelo = new DefaultTableModel(cabecera, 0);
        tbl_distribuciones.setModel(modelo);
    }//GEN-LAST:event_chb_distribucionPorDefectoActionPerformed

