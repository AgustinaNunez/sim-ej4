    private void btn_guardarDatosDistribucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarDatosDistribucionActionPerformed
        // validaciones de los campos
        lbl_error.setVisible(false);
        boolean todoOK = true;

        int maxReservacionesPermitidas = validarCampoNumericoTxt(txt_maxReservacionesPermitidas);
        if (maxReservacionesPermitidas == -1 || maxReservacionesPermitidas <= 0) {
            todoOK = false;
        }
        int minReservacionesPermitidas = validarCampoNumericoTxt(txt_minReservacionesPermitidas);
        if (minReservacionesPermitidas == -1 || minReservacionesPermitidas <= 0) {
            todoOK = false;
        }
        if (minReservacionesPermitidas > maxReservacionesPermitidas) {
            todoOK = false;
        }
        if (!todoOK) {
            lbl_error.setVisible(true);
            return;
        }

        // habilitaciones/deshabilitaciones de campos
        txt_maxReservacionesPermitidas.setEnabled(false);
        txt_minReservacionesPermitidas.setEnabled(false);
        btn_guardarDatosDistribucion.setEnabled(false);
        btn_guardarDistribucion.setEnabled(true);

        // aca se crea el modelo de la tabla para la carga de distribuciones
        String[] cabecera = {"Pasajeros", "Prob."};
        int rows = maxReservacionesPermitidas - minReservacionesPermitidas + 1;
        DefaultTableModel modelo = new DefaultTableModel(cabecera, rows);
        int aux = minReservacionesPermitidas;
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.setValueAt(aux++, i, 0);
            modelo.setValueAt(0.0, i, 1);
        }
        tbl_distribuciones.setModel(modelo);
    }//GEN-LAST:event_btn_guardarDatosDistribucionActionPerformed

    private void btn_guardarDistribucionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarDistribucionActionPerformed
        lbl_error.setVisible(false);
        // verificar que la suma de todas las probabilidades sea igual a 1
        if (Distribucion.verificarModelo((DefaultTableModel) tbl_distribuciones.getModel())) {
            // se crea la distribucion, cargándola en la Simulacion
            DefaultTableModel modelo = (DefaultTableModel) tbl_distribuciones.getModel();
            Simulacion.cargarModeloDistribucion(modelo);
            
            // aca va a la ventana de la simulación
            Principal ventana = new Principal();
            ventana.setLocationRelativeTo(null);
            ventana.setVisible(true);
            return;
        }
        // avisa que los datos no son válidos
        lbl_error.setVisible(true);
    }//GEN-LAST:event_btn_guardarDistribucionActionPerformed

    private int validarCampoNumericoTxt(javax.swing.JTextField txt) {
        int n = -1;
        try {
            n = Integer.parseInt(txt.getText());
        } catch (NumberFormatException e) {
            return n;
        }
        return n;
    }

    private void habilitarCampos(boolean flag) {
        // carga de valores por defecto
        txt_maxReservacionesPermitidas.setText("64");
        txt_minReservacionesPermitidas.setText("58");

        // habilitar/deshabilitar campos
        txt_maxReservacionesPermitidas.setEnabled(flag);
        txt_minReservacionesPermitidas.setEnabled(flag);
        btn_guardarDatosDistribucion.setEnabled(flag);
        tbl_distribuciones.setEnabled(flag);
        btn_guardarDistribucion.setEnabled(!flag);
    }

